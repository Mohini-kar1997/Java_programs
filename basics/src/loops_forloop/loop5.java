package loops_forloop;

public class loop5 {

	public static void main(String[] args) {
		// 1 3 5 7 9
		for(int i=1;i<=9;i=i+2) {
			System.out.print(i);
		}
		System.out.println();
		
		// print good evevning 20 times
		for(int i=0; i<=20;i++) {
			System.out.println("good evening");
		}

	}

}
