package loops_forloop;

public class loop8 {

	public static void main(String[] args) {
		//0 to 100
		System.out.println("0 to 100 numbers");
		for(int i=0;i<=100;i++) {
			System.out.println(i);
		}
		
		//100 to 0
		
		System.out.println("100 to o");
        for(int i=100;i>=0;i--) {
        	System.out.println(i);
        }
	}

}
